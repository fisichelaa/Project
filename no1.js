function Tampilkan(){
	var namkot=document.getElementById("form1").select1.value;

	if(namkot=="Aceh"){
		alert("Mie Aceh");
	}

	else if(namkot=="Sumatera Utara"){
		alert("Bika Ambon");
	}

	else if(namkot=="Maluku"){
		alert("Ikan Asar");
	}

	else if(namkot=="Banten"){
		alert("Sate Bandeng");
	}
	else if(namkot=="Bali"){
		alert("Ayam Betutu");
	}
}